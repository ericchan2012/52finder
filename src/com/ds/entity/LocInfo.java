package com.ds.entity;

public class LocInfo {

	private String mX;
	private String mY;
	private String mDetail;

	public String getmX() {
		return mX;
	}

	public void setmX(String mX) {
		this.mX = mX;
	}

	public String getmY() {
		return mY;
	}

	public void setmY(String mY) {
		this.mY = mY;
	}

	public String getmDetail() {
		return mDetail;
	}

	public void setmDetail(String mDetail) {
		this.mDetail = mDetail;
	}

}
